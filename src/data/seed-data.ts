export function galleryForType(type: string, propertyIndex = 0) {
  let gallery = galleries.home;
  if (["Đất nền", "Đất thổ cư", "Đất đầu tư", "Đất mặt đường"].includes(type))
    gallery = galleries.land;
  if (type === "Chung cư") gallery = galleries.apartment;
  if (["Shophouse", "Mặt bằng kinh doanh"].includes(type))
    gallery = galleries.commercial;
  if (["Nhà vườn", "Biệt thự"].includes(type)) gallery = galleries.garden;

  const offset = Math.abs(propertyIndex) % gallery.length;
  return [...gallery.slice(offset), ...gallery.slice(0, offset)];
}

